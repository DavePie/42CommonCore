int	add(int i, int j)
{
	return (i + j);
}

int	sub(int i, int j)
{
	return (i - j);
}

int	div(int i, int j)
{
	return (i / j);
}

int	mult(int i, int j)
{
	return (i * j);
}

int	mod(int i, int j)
{
	return (i % j);
}
